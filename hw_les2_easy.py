# Задача-1:
# Дан список фруктов.
# Напишите программу, выводящую фрукты в виде нумерованного списка,
# выровненного по правой стороне.

# Пример:
# Дано: ["яблоко", "банан", "киви", "арбуз"]
# Вывод:
# 1. яблоко
# 2.  банан
# 3.   киви
# 4.  арбуз
# Подсказка: воспользоваться методом .format()



FruList = ['ApricotAvocado', 'Pawpaw', 'Quince', 'Pineapple', 'Banana', 'Bergamot', 'Grape', 'Pomegranate', 'Grapefruit', 'Guava', 'Jackfruit']
for i, fruit in enumerate(FruList, start=1):
    print(i, '{:>15}'.format(fruit))

